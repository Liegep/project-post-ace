export type BriefLocale = 'pt' | 'en' | 'es' | 'it' | 'sv';

export const briefLocaleNames: Record<BriefLocale, string> = {
  pt: 'Português',
  en: 'English',
  es: 'Español',
  it: 'Italiano',
  sv: 'Svenska',
};

export function tLabel(templateId: string, questionId: string, locale: BriefLocale): string | undefined {
  if (locale === 'pt') return undefined; // fallback to default
  return labels[locale]?.[`${templateId}.${questionId}`];
}

export function tOptions(templateId: string, questionId: string, locale: BriefLocale): string[] | undefined {
  if (locale === 'pt') return undefined;
  return options[locale]?.[`${templateId}.${questionId}`];
}

export function tMeta(templateId: string, field: 'name' | 'desc', locale: BriefLocale): string | undefined {
  if (locale === 'pt') return undefined;
  return labels[locale]?.[`${templateId}._${field}`];
}

// UI strings used in the brief form
export function tUI(key: string, locale: BriefLocale): string {
  return uiStrings[locale]?.[key] || uiStrings.pt[key] || key;
}

